package DataType_And_Typecasting;

public class DatatypeList {

	public static void main(String[] args) {
		/* Datatype:
		 * Datatype is used to allocate memory blocks for the variable
		 */
		// To Find byte datatype using Wrapper class with Size
System.out.println("Size of Byte Datatype " + Byte.SIZE + " Bits");
System.out.println("Size of Short Datatype " + Short.SIZE + " Bits");
System.out.println("Size of Int Datatype " + Integer.SIZE + " Bits");
System.out.println("Size of Float Datatype " + Float.SIZE + " Bits");
System.out.println("Size of Double Datatype " + Double.SIZE + " Bits");
System.out.println("Size of Long Datatype " + Long.SIZE + " Bits");

	}
	

}
