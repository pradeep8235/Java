package DataType_And_Typecasting;

public class TypeCasting {

	public static void main(String[] args) {
	// Smaller Datatype into Bigger Datatype	
	Byte byteValue=5; // 8 Bits
	Integer intValue = new Integer(byteValue); // 32 Bits
	System.out.println(intValue);
	Integer intValue1 = 15;					// 32 Bits
	Double doubleValue = new Double(intValue1); // 64 Bits
	System.out.println(doubleValue);
	// Narrow Type Casting //
	// Real Time Example : ATM Screen

	}

}
