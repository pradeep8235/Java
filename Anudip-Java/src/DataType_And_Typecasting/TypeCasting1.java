package DataType_And_Typecasting;

public class TypeCasting1 {

	public static void main(String[] args) {
		
		String x="123";
		String y="456";
		System.out.println(x+y);
		// String + String = Concatenate or Merge
		// Integer + Integer = Addition 
		// String + Integer = (Statement) + Printvalue
			// int a=10;
			// Example: "Result " + a
			// 123 + 456
		System.out.println(Integer.parseInt(x)+Integer.parseInt(y));

	}

}
