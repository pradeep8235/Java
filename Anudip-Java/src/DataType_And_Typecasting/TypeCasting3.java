package DataType_And_Typecasting;

public class TypeCasting3 {

	public static void main(String[] args) {
		double data = 15.358;
		// Explicit Typecasting
		// Bigger value into Smaller value 
		Integer intValue = (int)data; // Explicit Type casting
		
		
		short data1 = 15;
		Byte byteValue = (byte)data1;
		
	}
}
