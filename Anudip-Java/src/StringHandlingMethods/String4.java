package StringHandlingMethods;

public class String4 {

	public static void main(String[] args) {
		
		String s1="Welcome to DeepTech";
		System.out.println("Length Of Given String " + s1.length());
		String s2 ="    Deep    Tech    ";
		System.out.println("Length is " + s2.length());
		System.out.println(s2);
		System.out.println(s2.trim());
		System.out.println(s2.trim().length());
		System.out.println(s2.length());
		// Strings are immutable can't be change once if it is initilized

	}

}
